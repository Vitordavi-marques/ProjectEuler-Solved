    public static void main(String[] args) {
        sepIntoArray();
        checkAdjNumbers();
    }
    
    protected static void sepIntoArray() {
        
        for (int c=1;c<=number.length();c++) {
            
            setDigit(c, Integer.parseInt( number.substring(c-1, c )) );
        }
    }
    
    protected static void checkAdjNumbers() {
        long bigM = 0;
        long m = 1;
        
        for (int c=1; c<= ((number.length()) - (i-1)); c++ ) {
            for (int d=c; d<= (c + i)-1; d++) {
                m = m * digits[i-1];
                
                if (d == ((c+i)-1)) {
                    System.out.print(digits[i-1] + " = ");
                } else {
                    System.out.print(digits[i-1] + " * ");
                }
            }
            
            System.out.print(m);
            System.out.println("");
            
            if (m > bigM) {
                bigM = m;
            }
            
            m = 1;
        }
        
        System.out.println(bigM);
    }

    public static void setDigit(int digit, int x) {
        digits[digit-1] = x;
    }
